def _set_para_text(p_el, text):
    """在克隆的段落元素上替换文字，保留首个 run 的 rPr（字体/字号/加粗）。"""
    runs = p_el.findall(qn("w:r"))
    if not runs:
        r = OxmlElement("w:r")
        p_el.append(r)
        runs = [r]
    first = runs[0]
    for r in runs[1:]:
        p_el.remove(r)
    for child in list(first):
        if child.tag in (qn("w:t"), qn("w:br"), qn("w:tab"), qn("w:cr")):
            first.remove(child)
    t = OxmlElement("w:t")
    t.set(qn("xml:space"), "preserve")
    t.text = text
    first.append(t)
    return p_el


def _clear_cell(cell):
    tc = cell._tc
    for p in tc.findall(qn("w:p")):
        tc.remove(p)


def _append_para(cell, p_el):
    cell._tc.append(p_el)


def fill_cell(cell, items, head_tmpl, body_tmpl):
    """用 (kind, text) 列表重建单元格，h 用楷体模板、b 用仿宋模板。"""
    _clear_cell(cell)
    for kind, text in items:
        tmpl = head_tmpl if kind == H else body_tmpl
        p_el = copy.deepcopy(tmpl)
        _set_para_text(p_el, text)
        _append_para(cell, p_el)


def fill_cell_own(cell, lines):
    """用单元格自身首段为模板，逐行重建（用于简单值/勾选框单元格，保留原字体）。"""
    own = copy.deepcopy(cell._tc.findall(qn("w:p"))[0])
    _clear_cell(cell)
    for line in lines:
        p_el = copy.deepcopy(own)
        _set_para_text(p_el, line)
        _append_para(cell, p_el)


def main():
    doc = docx.Document(SRC)
    tables = doc.tables

    # 从“(一)功能介绍”内容单元格取公文格式模板（楷体小标题 / 仿宋正文）
    src_cell = tables[3].rows[1].cells[0]
    head_tmpl = None
    body_tmpl = None
    for p in src_cell.paragraphs:
        txt = p.text.strip()
        if not txt:
            continue
        if head_tmpl is None and txt.startswith("（"):
            head_tmpl = copy.deepcopy(p._p)
        elif body_tmpl is None and not txt.startswith("（"):
            body_tmpl = copy.deepcopy(p._p)
        if head_tmpl is not None and body_tmpl is not None:
            break
    if head_tmpl is None:
        head_tmpl = body_tmpl
    if body_tmpl is None:
        body_tmpl = head_tmpl

    # 基本信息表（Table 2，索引1）
    t2 = tables[1]
    fill_cell_own(_distinct(t2, 1, 1), [APPLY_UNIT])
    fill_cell_own(_distinct(t2, 3, 1), APPLICATION_SCENARIO)
    fill_cell_own(_distinct(t2, 4, 1), PRODUCT_FORM)
    fill_cell(_distinct(t2, 6, 0), [(B, SUMMARY)], head_tmpl, body_tmpl)

    # 三、作品内容六大节（Table 4..9，索引3..8）的内容单元格 R1C0
    fill_cell(tables[3].rows[1].cells[0], SECTION_FUNC, head_tmpl, body_tmpl)
